	def search_random(query)
		result = search(query)
		if !result
			return nil
		end
		result[rand(result.length)]
	end

	def search_random_img(query)
		idol = search_random query
		if idol.nil?
			return nil
		end
		to_mobamasu_image_url(idol["ID"], query[:frame])
	end

	def parse_request(request, prefix = "#mobamasul?!?")
		if request !~ /#{prefix}[\s　].*/
			return nil
		end
		(op, search_word, *args) = request.split(/[\s　]+/, 4)
		if search_word.nil?
			return nil
		end

		rarity = nil
		regexp = nil
		args.each do |arg|
			if arg =~ /^(N|N\+|R|R\+|SR|SR\+)(,(N|N\+|R|R\+|SR|SR\+))*$/
				rarity = arg
			end
			if arg =~ /^\/.*\/$/
				regexp = Regexp.new(NKF::nkf('-WwXm0', arg[1..-2]))
			end
		end
		frame = true
		if op =~ /#mobamasu!/
			frame = false
		end

		search_word = Romaji.romaji2kana search_word, :kana_type => :hiragana if search_word =~ /\w/
		{ :name => search_word, :rarity => rarity, :frame => frame, :regexp => regexp }
	end


	def search_loading(query)
		names = Mobamasu.to_fullname(query[:name]).map { |it| NKF.nkf("-w -x -Z4", it) }

		url = "http://imcgcollector.blog.fc2.com/blog-entry-994.html"

		agent = Mechanize.new
		page = agent.get(url)
		tables = (page/:table)[5, 17]

		chars = tables.search(:tr).select { |tr|
			td = tr/:td
			   (td[0] && names.find { |it| td[0].inner_text =~ /#{it}/ } && td[2][:bgcolor] != "#cccccc")	\
			|| (td[1] && names.find { |it| td[1].inner_text =~ /#{it}/ } && td[3][:bgcolor] != "#cccccc")
		}

		chars.map! { |it|
			name = (it/:td)[0].inner_text
			name = (it/:td)[1].inner_text if name =~ /^[★◆]$/
			{
				:name => name.gsub(/.?[\(（]\d*[\)）].?/, ""),
				:katagaki  => (it/:a)[1][:href],
				:loading_icon  => (it/:a)[2][:href]
			}
		}
		if query[:regexp]
			regexp = /#{NKF.nkf("-w -x -Z4", Romaji.romaji2kana(query[:regexp].source))}/
			chars.select { |it| it[:name] =~ regexp }
		else
			chars
		end
	end

