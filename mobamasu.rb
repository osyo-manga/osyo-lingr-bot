	def to_fullname(name)
		name = Romaji.romaji2kana name, :kana_type => :hiragana if name =~ /\w/
		name_r = /#{name}/
		
		result = CHARACTER_LIST.select { |chara|
			chara["name_ruby"] =~ name_r
		}.map { |it| it["name"] }
		result.empty? ? [name] : result
	end


	def rarity_to_n(rarity)
		case rarity
		when 'N'
			1
		when 'N+'
			2
		when 'R'
			3
		when 'R+'
			4
		when 'SR'
			5
		when 'SR+'
			6
		else
			1
		end
	end

	def search(query)
		response = Net::HTTP.get "ppdb.sekai.in", "/api/2/idol.json?full=1&name=#{ERB::Util.url_encode query[:name]}"
		result = JSON.parse(response)
		if result["result"] == false
			return nil
		end

		idols = result["data"]
		if query[:regexp]
			regexp = /#{NKF.nkf("-w -x -Z4", Romaji.romaji2kana(query[:regexp].source))}/i
			idols.select! { |it| it["Name"] =~ regexp }
		end
		if query[:rarity] == nil
			return idols
		end

		rarity = rarity_to_n query[:rarity]
		idols.select! { |idol|
			idol["Rarity"] == rarity
		}
	end


